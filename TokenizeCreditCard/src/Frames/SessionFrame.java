    private void btnTokenizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTokenizeActionPerformed
        // TODO add your handling code here:
        String cardIdString = txtCreditCard.getText();
        String token = "";
        if(Validation.validateCreditCard(cardIdString)){
            token = Tokenization.tokenize(cardIdString);
            txtToken.setText(token);
            CreditCard card = new CreditCard(cardIdString, token);
            communication.sendMessage(card, "Tokenize");
        }
        else{
            JOptionPane.showMessageDialog(null, "Not a valid credit card number");
        }


        
        
    }//GEN-LAST:event_btnTokenizeActionPerformed

