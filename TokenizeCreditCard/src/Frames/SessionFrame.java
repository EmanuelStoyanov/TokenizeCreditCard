    private void btnUntokenizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUntokenizeActionPerformed
        // TODO add your handling code here:
        String token = txtToken.getText();
        communication.sendMessage(token, "Untokenize");
        String card = "";
        
        try {
            card = (String) communication.recieveObject();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SessionFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtCreditCard.setText(card);
        
    }//GEN-LAST:event_btnUntokenizeActionPerformed

